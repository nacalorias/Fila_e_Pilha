// Ela serve apenas para guardar três informações juntas.
public class Elemento {
    // Variáveis para guardar os dados (strings).
    String campo1;
    String campo2;
    String campo3;


    // Construtor: é chamado quando um novo 'Elemento' é criado.
    // Ele recebe os três valores e os armazena nas variáveis da classe.
    public Elemento(String c1, String c2, String c3) {
        this.campo1 = c1;
        this.campo2 = c2;
        this.campo3 = c3;
    }
}
